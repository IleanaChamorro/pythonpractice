#En python, la función len() nos permite obtener la longitud de una cadena de caracteres, o bien, el número de elementos que componen un objeto.
#La sintaxis correcta para poder utilizar la función len(), es la siguiente:
#len("Hola")

#Comportamiento de la función len()
print(len("Hola"))

#Opción 1
print("Hola tiene", len("Hola"), "caracteres")

#Opcion 2
longitud = len("Ejemplo cadena")
print("Ejemplo cadena tiene", longitud, "caracteres")